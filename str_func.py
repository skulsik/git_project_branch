def str_up(str):
    """
    Функция выполняет перевод всех символов в строке в верхний регистр
    Возвращает модифицированную строку
    """
    return str.upper()


def str_word_up(str):
    """
    Функция выполняет перевод первых символов в словах - в верхний регистр
    Возвращает модифицированную строку.
    """
    return str.title()
